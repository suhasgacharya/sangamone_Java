package com.sangamone;

public class Math {

	public static void main(String[] args) {
		int count=101;
		for (int i=0;i<(count/2)+1;i++) {
			System.out.print(2*i+" ");
			}
		
		System.out.println();
		for (int i=0;i<(count/3)+1;i++) {
			System.out.print(3*i +" ");
		}
		
		System.out.println();
		for (int i=0;i<(count/4)+1;i++) {
		System.out.print(4*i +" ");
		}
	
	}

}